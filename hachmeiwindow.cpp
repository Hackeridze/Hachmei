// Consider the date to numerologic number
unsigned int HachmeiWindow::DateConsider()
{
    if (isCurrentDateValid())
    {
        unsigned int d,m,y;
        y = ui->comboBox_3->currentText().toInt();
        m = ui->comboBox_2->currentText().toInt();
        d = ui->comboBox->currentText().toInt();

        int summ = getSummOfNumbers(
                    getSummOfNumbers(y) +
                    getSummOfNumbers(m) +
                    getSummOfNumbers(d)
                    );
        return summ;
    }

    return 0;
}

// Consider the first, second and middle names to numerologic number
unsigned int HachmeiWindow::FIOConsider()
{
    QTextCodec *RusCodec = QTextCodec::codecForName("utf-8"); // make codec
    QTextCodec::setCodecForCStrings(RusCodec); // Installing codec

    //-------------Second Name Considering--------------
    QString secondName = ui->lineEdit->text();
    secondName = secondName.trimmed().simplified().toLower();
    QChar* sna = secondName.data();
    int lenSN = secondName.size();

    int SNSumm = 0;
    for (int i=0;i<lenSN;i++)
    {
        int cch = sna[i].unicode();
        SNSumm += getCharNum(cch);
    }
    SNSumm = getSummOfNumbers(SNSumm);
    if(SNSumm == 0)
    {
        QMessageBox::critical(this,QString::fromLocal8Bit("Ошибка! :'("),QString::fromLocal8Bit(
                "<b>А где фимилия?</b> Пожалуйста, укажите Вашу фамилию!"
            ), QMessageBox::Ok, QMessageBox::NoButton);
        return 999;
    }


    //-------------First Name Considering---------------
    QString name = ui->lineEdit_2->text();
    name = name.trimmed().simplified().toLower();
    QChar* fna = name.data();
    int lenFN = name.size();
    int FNSumm = 0;
    for (int i=0;i<lenFN;i++)
    {
        int cch = fna[i].unicode();
        FNSumm += getCharNum(cch);
    }
    FNSumm = getSummOfNumbers(FNSumm);

    if(FNSumm == 0)
    {
        QMessageBox::critical(this,QString::fromLocal8Bit("Ошибка! :'("),QString::fromLocal8Bit(
                "<b>А где имя?</b> Пожалуйста, укажите Ваше имя!"
            ), QMessageBox::Ok, QMessageBox::NoButton);
        return 999;
    }

    //-------------Middle Name Considering--------------
    QString middleName = ui->lineEdit_3->text();
    middleName = middleName.trimmed().simplified().toLower();
    QChar* mna = middleName.data();
    int lenMN = middleName.size();

    int MNSumm = 0;
    for (int i=0;i<lenMN;i++)
    {
        int cch = mna[i].unicode();
        MNSumm += getCharNum(cch);
    }
    MNSumm = getSummOfNumbers(MNSumm);

    if(MNSumm == 0)
    {
        QMessageBox::critical(this,QString::fromLocal8Bit("Ошибка! :'("),QString::fromLocal8Bit(
                "<b>А где отчество?</b> Пожалуйста, укажите Ваше отчество!"
            ), QMessageBox::Ok, QMessageBox::NoButton);
        return 999;
    }

    return getSummOfNumbers(SNSumm + FNSumm + MNSumm);
}

unsigned int HachmeiWindow::GoldConsider(unsigned int dateSumm, unsigned int FIOSumm)
{
    return getSummOfNumbers(dateSumm + FIOSumm);
}

