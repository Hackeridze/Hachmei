unsigned int HachmeiWindow::getSummOfNumbers(unsigned int oprnd)
{
    int is = 999;
    int counter = 10;
    int ret = 0;
    while(oprnd > 0)
    {
        is = oprnd%counter;
        oprnd = oprnd/counter;
        ret += is;
    }
    if (ret > 9)
    {
        ret = getSummOfNumbers(ret);
    }
    return ret;
}

