// Check is the inserted date valid or not
bool HachmeiWindow::isCurrentDateValid()
{
    if (QDate(ui->comboBox_3->currentText().toInt(),
              ui->comboBox_2->currentText().toInt(),
              ui->comboBox->currentText().toInt()
              ).isValid())
    {
        return true;
    }
    else // Not valid :'(
    {
        QMessageBox::critical(this,QString::fromLocal8Bit("Ошибка! :'("),QString::fromLocal8Bit(
                              "<b>Нет такой даты!</b> Пожалуйста, укажите корректную дату!"
                          ), QMessageBox::Ok, QMessageBox::NoButton);
        return false;
    }
}

