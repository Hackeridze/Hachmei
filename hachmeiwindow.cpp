void HachmeiWindow::LoadCB()
{
    for(int i = 1;i<=31;i++){ // For days
        QString str = QString::number(i);
        ui->comboBox->addItem(str, i-1);
    }

    for(int i = 1;i<=12;i++){ // For months
        QString str = QString::number(i);
        ui->comboBox_2->addItem(str, i-1);
    }

    int ye = QDate::currentDate().year(); // get current year
    for(int i = 1900;i<=ye;i++){ // For years(from 1900 to current)
        QString str = QString::number(i);
        ui->comboBox_3->addItem(str, i-1);
    }

    ui->comboBox->setCurrentIndex(0);
    ui->comboBox_2->setCurrentIndex(0);
    ui->comboBox_3->setCurrentIndex(0);
}

